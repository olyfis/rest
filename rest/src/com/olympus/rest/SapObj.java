package com.olympus.rest;

public class SapObj {

	private int creditApp;
	private int numAssets;
	private int numQuotes;
	private double equipCost;
	
	
	public int getCreditApp() {
		return creditApp;
	}
	public void setCreditApp(int creditApp) {
		this.creditApp = creditApp;
	}
	public int getNumAssets() {
		return numAssets;
	}
	
	public void setNumAssets(int numAssets) {
		this.numAssets = numAssets;
	}
	public int getNumQuotes() {
		return numQuotes;
	}
	public void setNumQuotes(int numQuotes) {
		this.numQuotes = numQuotes;
	}
	public double getEquipCost() {
		return equipCost;
	}
	public void setEquipCost(double equipCost) {
		this.equipCost = equipCost;
	}
	
	
	
	
}
